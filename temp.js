        function playPanelAnimation(containerId, overrideBasin = null) { 
            clearInterval(panelPlayInterval); 
            let basinName = overrideBasin || document.getElementById('basin-title').innerText;
            if(!basinData[basinName]) basinName = 'Türkiye Geneli'; 

            let d = basinData[basinName];
            let currentHist = fullDates.map(()=>'-'); 
            let currentPred = fullDates.map(()=>'-'); 
            let index = 0; 
            
            activePanelChart.setOption(getEChartsOption(basinName, currentHist, currentPred)); 
            
            panelPlayInterval = setInterval(() => { 
                if(index >= fullDates.length) { clearInterval(panelPlayInterval); return; } 
                for(let i=0; i<=index; i++) {
                    if(fullDates[i] < "2025-04") currentHist[i] = d.hist[i];
                    else currentPred[i] = d.pred[i];
                }
                activePanelChart.setOption({ series: [{ data: currentHist }, { data: currentPred }] }); 
                index += 4; 
            }, 30); 
        }

        function createNodes(basinName, centerCoords) {
            const lng = centerCoords[0]; const lat = centerCoords[1];
            const specItems = basinMenuItems[basinName] || [];
            
            const nodeData = [
                { coords: [lng - 0.4, lat + 0.4], label: 'TWSA AI Tahmini', target: 'panel-twsa' },
                { coords: [lng - 0.4, lat - 0.4], label: specItems[0] || 'Kritik Senaryo', target: 'panel-scenario' },
                { coords: [lng + 0.4, lat + 0.4], label: specItems[1] || 'Görsel Simülasyon', target: 'panel-visual' },
                { coords: [lng + 0.4, lat - 0.4], label: specItems[2] || 'Su Stresi', target: 'panel-visual' } 
            ];

            nodeData.forEach((data, index) => {
                const el = document.createElement('div'); el.className = 'node-marker';
                const label = document.createElement('div'); label.className = 'node-label'; label.innerText = data.label; el.appendChild(label);
                const marker = new mapboxgl.Marker(el).setLngLat(data.coords).addTo(map); nodes.push(marker);
                setTimeout(() => { el.style.opacity = '1'; }, index * 200);

                el.addEventListener('click', () => {
                    map.flyTo({ center: data.coords, zoom: 7.8, pitch: 0, speed: 0.5 });
                    let cHtml = panelContents[basinName] && panelContents[basinName][data.label] ? panelContents[basinName][data.label] : '<div style="color:#888;">[Veri Bekleniyor]</div>';
                    if(data.target === 'panel-scenario') {
                        document.getElementById('scenario-title').innerText = data.label;
                        document.getElementById('scenario-content').innerHTML = cHtml;
                        document.getElementById('scenario-content').style.border = 'none';
                        document.getElementById('scenario-content').style.background = 'transparent';
                    }
                    if(data.target === 'panel-visual') {
                        document.getElementById('visual-title').innerText = data.label;
                        document.getElementById('visual-content').innerHTML = cHtml;
                        document.getElementById('visual-content').style.border = 'none';
                        document.getElementById('visual-content').style.background = 'transparent';
                    }
                    openPanel(data.target);
                    setTimeout(() => renderMiniCharts(basinName, data.label), 200);
                    // Butona tıklayınca doğru havzanın grafiğini çizer
                    if (data.target === 'panel-twsa') { setTimeout(() => { initPanelECharts('echarts-container', basinName); }, 200); }
                });
            });
        }

